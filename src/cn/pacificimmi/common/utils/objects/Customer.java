package cn.pacificimmi.common.utils.objects;

public class Customer{
	
	private	int s_custid;
	
	private	String nick_name;
		
	private	String cust_name;
		
	private	String gender;
		
	private	String mobile;
		
	private	String cust_status;
		
	private	String wechat;
		
	private	String email;
		
	private	String address;
		
	private	String cust_referrer;
		
	private	long create_time;
		
	private	String mark;

	public int getS_custid() {
		return s_custid;
	}

	public void setS_custid(int s_custid) {
		this.s_custid = s_custid;
	}

	public String getNick_name() {
		return nick_name;
	}

	public void setNick_name(String nick_name) {
		this.nick_name = nick_name;
	}

	public String getCust_name() {
		return cust_name;
	}

	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getCust_status() {
		return cust_status;
	}

	public void setCust_status(String cust_status) {
		this.cust_status = cust_status;
	}

	public String getWechat() {
		return wechat;
	}

	public void setWechat(String wechat) {
		this.wechat = wechat;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCust_referrer() {
		return cust_referrer;
	}

	public void setCust_referrer(String cust_referrer) {
		this.cust_referrer = cust_referrer;
	}

	public long getCreate_time() {
		return create_time;
	}

	public void setCreate_time(long create_time) {
		this.create_time = create_time;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}
}